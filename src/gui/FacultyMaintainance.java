    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        String selectedFaculty = (String)deleteComboBox.getSelectedItem();
        System.out.println(selectedFaculty);
        HashMap<String, Faculty> faculties = systemData.getFaculties();
        faculties.remove(selectedFaculty);        
        systemData.setFaculties(faculties);
        deleteComboBox.removeItem(selectedFaculty);
        editComboBox.removeItem(selectedFaculty);
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        panelHolder.setTitle("Edit Faculty");
        panelHolder.getContentPane().removeAll();
        panelHolder.getContentPane().add(new FacultyAddEdit(panelHolder, systemData,
                true, systemData.getFaculties().get(editComboBox.getSelectedItem())));
        panelHolder.getContentPane().revalidate();        
        //panelHolder.pack();        
        
    }//GEN-LAST:event_editButtonActionPerformed

    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        panelHolder.setTitle("Add new Faculty");
        panelHolder.getContentPane().removeAll();
        panelHolder.getContentPane().add(new FacultyAddEdit(panelHolder, systemData, false, null));
        panelHolder.getContentPane().revalidate();        
        //panelHolder.pack();        
    }//GEN-LAST:event_addButtonActionPerformed


